                        <h1>Calls for Papers : “Epistemology and Indian Logic”</h1>
                        <br>
                        <p>
                            <strong>CONCEPT NOTE</strong>: The rationality of the theme “epistemology and Indian logic” lies in the                             fact that logic in India has been part and parcel of different  <i>systems</i> of philosophy and                                   therefore has been inseparably linked to their epistemological and metaphysical beliefs. As there is no                             single system called ‘Indian philosophy’, Indian logic is also studied in                                                           a systemic form such as the Nyāya logic, the Buddhist logic and the Jaina logic. Despite a wide range                               of differences in the metaphysical beliefs of different systems, there are some recognizable                                       similarities in epistemological thinking. For example, there are focused debates on the nature and                                 function of knowledge, number of the sources of knowledge, the conditions leading to illusory cognition                             and the role of reasoning and testimony in dispelling the ignorance—a sine qua non to the attainment of                             the ultimate objective. These debates drove the attention of thinkers to the patterns of reasoning as                               early as in the second century B.C.E. The modern sense of logic is traced back into the intellectual                               activity called <i>anvīkṣā</i> (investigation) which consists in the reviewing <i>(anu-īkṣaṇa)</i> of a                             thing previously apprehended <i>(īkṣita)</i> through perception or verbal cognition. The science that                               makes this activity as the subject matter of its study is variously named <i>ānvīkṣikī</i>,                                         <i>nyāyavidyā</i>, or <i>hetuvidyā</i>, the ‘science of reasoning’ (logic). <i>Anumāna</i>                                         (<i>‘anu’</i> means ‘after’ and <i>‘māna’</i> means ‘knowledge’) thus becomes a natural term to refer                                to the process of reasoning.
                        </p>
                        <br>
                        <p>
                            What is significant in the above definition of logic is the nuptial link of inferential knowledge to a                             previously acquired knowledge, perceptually or otherwise. Logic, in the classical Indian philosophy, is                             therefore discussed as a part of epistemology, not as an independent discipline. And, the manifest goal                             of inference is to generate true cognition or knowledge whether for oneself or for other, and not to                               prove validity or invalidity. What is crucial to the production of inferential knowledge is the                                     necessary relationship between the reason <i>(hetu)</i> and the claim <i>(sādhya)</i>. A careful and                               elaborate discussion on ascertaining and apprehending this relationship is seen in the Indian logical                               tradition. It is also observed that a great amount of energy is invested by the Indian logicians in                                 establishing the universal proposition (<i>udāharaṇa</i>—the statement of necessary relation between                               the <i>hetu</i> and the <i>sādhya</i> along with actual instance) than developing formal techniques of                             reasoning. This is not to suggest that the discussions do not involve any structure. The logical                                   section of the <i>Carakasaṁhitā</i> (c. first century C.E. text on health care) delineates the methods                             of discussion. It is not surprising to see the elaboration of the technique in this text given the                                 urgent nature of the medical profession. On the basis of the symptoms <i>(hetu)</i> the medical                                     professionals ascertain the presence of a particular disease <i>(sādhya)</i> in a body. The certainty                               of their inferential knowledge enables them to prescribe suitable treatment.           
                        </p>
                        <br>
                        <p>
                            The pragmaticity of the above kind however has not hindered the Indian logicians to delve deeply on                                 various issues pertaining to generation and authenticity of inferential knowledge. After particularly                               4th or 5th century C.E., a plethora of texts can be seen undertaking logical issues for separate                                   discussions. There has not been a concerted effort to explore the insights available in the Indian                                 logical thinking. The discussion is required from not only historical perspective but also in relation                             to the concepts developed in rational sequence. For example, whereas the ancient thinkers relied on the                             analogical method of reasoning, the later thinkers engaged in <i>hetu</i>-centric discussion, the                                   <i>Navya-nyāya</i> method being the latest one. Similarly, whereas the non-Buddhist thinkers adopted                               primarily the technique of demonstration, the Buddhist thinkers opted primarily for the technique of                               refutation. What could have been the reason for such development? An answer to this question may                                   reaffirm the close ties between the logical thinking and epistemological and metaphysical beliefs. The                             participants of the seminar are expected to stimulate the discussion of theoretical and/or practical                               relevance. The following are the suggestive themes:
                        </p>
                        <br>
                        <ul>
                            <li>The Concept of Pramāṇa</li>
                            <li>Analgocal Reasoning in Ancient Debates</li>
                            <li>The Question of Truth, Validity and Soundness with regard to <i>Anumāna</i></li>
                            <li>The Nature of Necessity between <i>Hetu</i> and <i>Sādhya</i></li>
                            <li><i>Anupalabdhi</i> and <i>Abhāva</i></li>
                            <li>Reducibility of the Sources of Knowledge</li>
                            <li>The Navya-nyāya Concept of Negation</li>
                            <li>The Issue of Psychologism in Indian Logic</li>
                            <li>The Jaina Synthesis of the Logical Structure</li>
                            <li>The Nature of Universal Proposition</li>
                            <li>The Concept of <i>Prasaṅga</i></li>
                            <li>The authors are expected to choose any topic from the wide range of Indian logical thinking.</li>
                        </ul>
                        <br>
                        <p>
                            Overseas participants are also expected in this section. Any reseach scholar can participate in this                               section by sending a good quality reseach paper within the stipulated time. The final list of speakers                             will be published after receiving research papers and confirmed participation.  
                        </p>
