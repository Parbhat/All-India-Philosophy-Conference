                        <div class="large-12 small-12 columns">
                            <h1  >
                            अखिल भारतीय दर्शन-परिषद्
                            </h1>
                            <h2  >
                            60वां अधिवेशन (03–05 जून 2015)
                            </h2>
                            <br>
                            <p  >
यह गौरव की बात है कि अखिल भारतीय दर्शन परिषद् का महत्त्वपूर्ण 60वां अधिवेशन 3 से 5 जून 2015 के मध्य आयोजित करने का दायित्व श्री माता वैष्णो देवी विश्वविद्यालय, कटड़ा, जम्मू-कश्मीर, को मिला है। इस विश्वविद्यालय की स्थापना श्री माता वैष्णो देवी श्राइन बोर्ड के पूर्ण वित्तीय सहयोग से सन् 1999 में हुई। यह विश्वविद्यालय हिमालय की गोद में स्थित त्रिकुटा पर्वतमालाओं के सुरम्य वातावरण में स्थापित किया गया है। सन् 2004 से यहाँ पर विज्ञान, तकनीकी, और मानविकी विषयों में स्नातक और परास्नातक स्तर पर अध्ययन-अध्यापन किया जा रहा है। 
                            </p>
                            <br>
                            <p  >
अखिल भारतीय दर्शन परिषद् के 60वें अधिवेशन के प्रधान सभापति का गौरव डॉ० ज्योतीन्द्र भाई दवे को प्रदान किया गया है जो स्वामीनारायण शोध संस्थान, नई दिल्ली के निदेशक पद पर सुशोभित हैं। डॉ० दवे की अभिरुचि दर्शन और संस्कृति जैसे गंभीर विषय में अनुसंधान की रही है, यह एक सुखद संयोग है कि इस अधिवेशन का मुख्य विषय “दर्शन एवं संस्कृति” रखा गया है। प्रतिवर्ष की भाँति इस बार भी अधिवेशन के शैक्षणिक कार्यक्रमों में व्याख्यानमालाओं एवं संगोष्ठियों का आयोजन किया जाएगा। इसके अतिरिक्त अधिवेशन की वरीयता को देखते हुए “Epistemology and Indian Logic” विषय पर एक अंतर्राष्ट्रीय संगोष्ठी का भी आयोजन किया जा रहा है। अन्य दो संगोष्ठियों के विषय निम्नलिखित हैं: 
                            
                            </p>
                            <p>
                            <ul id="topics">
                            <li><p>कश्मीर शैव-दर्शन एवं सौंदर्यशास्त्र की भारतीय परम्परा</p></li>
                            <li><p>अंतर-साभ्यतिक संघर्ष और संवाद की संभावना</p></li>
                            </ul>
                            </p>
                        <div class="row" id="important">
                            <div class="large-6 small-6 columns text-left">
                                <br>
                                <br>
                                <h1>महत्त्वपूर्ण तिथियाँ: </h1>
                                <hr>
                                <p>पंजीकरण-शुल्क एवं सार-पत्र भेजने की अन्तिम तिथि: <i> 23 मार्च 2015 </i></p>
                                <p>सार-पत्र स्वीकृति की उद्घोषणा तिथि: <i> 30 मार्च 2015</i></p>
